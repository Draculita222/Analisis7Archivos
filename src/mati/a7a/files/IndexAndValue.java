package mati.a7a.files;

public class IndexAndValue {
	
	public final int index;
	public final String value;
	
	public IndexAndValue(int index, String value) {
		super();
		this.index = index;
		this.value = value;
	}

	public int getIndex() {
		return index;
	}

	public String getValue() {
		return value;
	}
	
}
